// use various math library operations on the numbers

#include <iostream>
#include <cmath>

using namespace std;

int main()
{
	cout << pow(5, 3) << endl;
	cout << sqrt(5) << endl;
	cout << round(5.443) << endl;
	cout << ceil(5.1) << endl;
	cout << floor(5.1) << endl;
	cout << fmax(5, 10) << endl;
	cout << fmin(5, 10) << endl;

	return 0;
}
